#!/usr/bin/env node
/*
 * validate-internal-links.mjs
 * ───────────────────────────
 * After `npm run build`, walks every HTML file in `dist/` and asserts that
 * every internal href (starting with `/`) resolves to an actual file in `dist/`
 * OR to a redirect rule in `public/_redirects` (Cloudflare Pages syntax).
 *
 * Catches typos like /customer-success-playbook/foo (missing the trailing
 * `s` on `playbooks`) that Astro happily builds and the user sees as 404 in
 * production.
 *
 * Ignored:
 *   - Fragment-only hrefs (`#...`)
 *   - Protocol-relative URLs (`//...`)
 *   - Absolute URLs (`http://`, `https://`, `mailto:`, `tel:`)
 *   - Pagefind runtime assets (`/pagefind/...`) — generated by the postbuild
 *     step but their internal references are managed by Pagefind itself
 *   - URLs that match a static prefix in `public/_redirects`
 *
 * Collects all violations before exiting.
 */

import { readFileSync, readdirSync, statSync } from 'node:fs';
import { resolve, join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const repoRoot = resolve(fileURLToPath(new URL('.', import.meta.url)), '../..');
const distDir = resolve(repoRoot, 'dist');
const redirectsPath = resolve(repoRoot, 'public/_redirects');

// ── 0. Sanity: dist/ must exist ────────────────────────────────────────────
try {
  statSync(distDir);
} catch {
  console.error(
    `✗ dist/ not found at ${distDir}. Run \`npm run build\` first.`
  );
  process.exit(1);
}

// ── 1. Build the set of resolvable URL paths from dist/ ────────────────────
const resolvablePaths = new Set();

function walkDir(dir) {
  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    const full = join(dir, entry.name);
    if (entry.isDirectory()) {
      walkDir(full);
    } else if (entry.isFile()) {
      // Strip the dist/ prefix and normalize.
      const rel = '/' + full.slice(distDir.length + 1).split('\\').join('/');
      resolvablePaths.add(rel);

      // Treat /foo/index.html as resolving to /foo/ AND /foo
      if (rel.endsWith('/index.html')) {
        const base = rel.slice(0, -'index.html'.length); // /foo/
        resolvablePaths.add(base);                       // /foo/
        if (base.length > 1) resolvablePaths.add(base.slice(0, -1)); // /foo
      }
      // Treat /foo.html as also resolving to /foo
      if (rel.endsWith('.html') && !rel.endsWith('/index.html')) {
        resolvablePaths.add(rel.slice(0, -'.html'.length));
      }
    }
  }
}
walkDir(distDir);
// The site root itself.
resolvablePaths.add('/');

// ── 2. Parse public/_redirects so links to redirected paths don't fail ────
const redirectSources = new Set();
try {
  const txt = readFileSync(redirectsPath, 'utf8');
  for (const line of txt.split('\n')) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('#')) continue;
    // Cloudflare _redirects format: `<from>  <to>  <status>`
    // We only need the <from> column.
    const [from] = trimmed.split(/\s+/);
    if (from && from.startsWith('/')) redirectSources.add(from);
  }
} catch {
  // _redirects is optional; silently ignore if missing.
}

// ── 3. Walk every built HTML file, extract internal hrefs ─────────────────
function isInternalHref(href) {
  if (!href.startsWith('/')) return false;
  if (href.startsWith('//')) return false;        // protocol-relative
  if (href.startsWith('/pagefind/')) return false; // pagefind runtime
  return true;
}

function normalizeHref(href) {
  // Strip query and fragment for the existence check.
  return href.split('#')[0].split('?')[0];
}

const hrefRegex = /href=(?:"([^"]+)"|'([^']+)'|([^\s>]+))/g;
const violations = new Map(); // href → Set<filePath>

function scanFile(filePath) {
  const html = readFileSync(filePath, 'utf8');
  for (const m of html.matchAll(hrefRegex)) {
    const raw = m[1] ?? m[2] ?? m[3];
    if (!raw || !isInternalHref(raw)) continue;
    const href = normalizeHref(raw);
    if (resolvablePaths.has(href) || redirectSources.has(href)) continue;
    if (!violations.has(href)) violations.set(href, new Set());
    violations.get(href).add(filePath.slice(distDir.length + 1));
  }
}

function walkHtml(dir) {
  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    const full = join(dir, entry.name);
    if (entry.isDirectory()) {
      // Skip pagefind's own assets (lots of internal refs to itself).
      if (entry.name === 'pagefind') continue;
      walkHtml(full);
    } else if (entry.isFile() && full.endsWith('.html')) {
      scanFile(full);
    }
  }
}
walkHtml(distDir);

// ── 4. Report ──────────────────────────────────────────────────────────────
if (violations.size > 0) {
  const total = [...violations.values()].reduce((n, set) => n + set.size, 0);
  console.error(
    `✗ Found ${violations.size} broken internal link${violations.size === 1 ? '' : 's'} ` +
      `across ${total} occurrence${total === 1 ? '' : 's'}:\n`
  );
  for (const [href, files] of [...violations.entries()].sort()) {
    console.error(`  ${href}`);
    for (const f of [...files].sort().slice(0, 5)) {
      console.error(`      ↳ ${f}`);
    }
    if (files.size > 5) {
      console.error(`      ↳ … and ${files.size - 5} more page${files.size - 5 === 1 ? '' : 's'}`);
    }
  }
  console.error('');
  process.exit(1);
}

console.log(
  `✓ Internal links resolve cleanly (scanned ${resolvablePaths.size} known paths, ${redirectSources.size} redirect rules)`
);
